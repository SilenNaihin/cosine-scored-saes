"""
MIB Track 2 (Causal Variable Localization) runner for RNH validation.

Creates a featurizer submission from a BenchSAE and runs MIB evaluation.

Constraint: MIB supports Qwen2.5-0.5B (not Qwen3-8B). To use this runner,
you need an SAE trained on Qwen2.5-0.5B. The adapter.py factories all work
with any model — just pass model_name="Qwen/Qwen2.5-0.5B".

Setup:
    git clone https://github.com/aaronmueller/MIB.git benchmarks/MIB
    cd benchmarks/MIB && git submodule update --init --recursive
    pip install -r MIB-causal-variable-track/requirements.txt

Usage:
    from benchmarks.adapter import BenchSAE
    from benchmarks.run_mib import prepare_submission

    sae = BenchSAE.from_custom_checkpoint(
        "checkpoints/qwen2.5_0.5b_sae.pt",
        hook_layer=12,
        model_name="Qwen/Qwen2.5-0.5B",
        device="cuda",
    )
    prepare_submission(sae, sae_name="rnh-cosine")

    # Then run:
    # python benchmarks/MIB/MIB-causal-variable-track/evaluate_submission.py \
    #     --submission_folder benchmarks/mib_submissions/rnh-cosine
"""

from __future__ import annotations

import os
from pathlib import Path

import torch
import torch.nn as nn

from benchmarks.adapter import BenchSAE, SAEFeaturizer, SAEInverseFeaturizer

# MIB supported models and their tasks/variables
MIB_MODELS = {
    "Qwen/Qwen2.5-0.5B": {
        "tasks": {
            "ioi": ["IO", "S"],
            "mcqa": ["answer"],
            "arc": ["correct_letter"],
        },
    },
    "google/gemma-2-2b": {
        "tasks": {
            "ioi": ["IO", "S"],
            "mcqa": ["answer"],
            "arc": ["correct_letter"],
        },
    },
}


def prepare_submission(
    sae: BenchSAE,
    sae_name: str,
    output_dir: str = "benchmarks/mib_submissions",
    model_name: str | None = None,
    tasks: dict[str, list[str]] | None = None,
) -> Path:
    """Create a MIB Track 2 submission folder from a BenchSAE.

    The submission format requires:
        {TASK}_{MODEL_SHORT}_{VARIABLE}/
            featurizer.pt        — state dict for featurizer module
            inverse_featurizer.pt — state dict for inverse featurizer module
            feature_indices.pt   — indices of features to intervene on (optional)

    Args:
        sae: A BenchSAE adapter.
        sae_name: Name for this submission.
        output_dir: Base directory for submissions.
        model_name: Override model name. Must be in MIB_MODELS.
        tasks: Override task/variable mapping. Defaults to all tasks for the model.

    Returns:
        Path to the submission folder.
    """
    model_name = model_name or sae.cfg.model_name

    if model_name not in MIB_MODELS:
        supported = ", ".join(MIB_MODELS.keys())
        raise ValueError(
            f"MIB does not support {model_name}. Supported: {supported}. "
            f"Train an SAE on one of these models to use MIB."
        )

    if tasks is None:
        tasks = MIB_MODELS[model_name]["tasks"]

    submission_dir = Path(output_dir) / sae_name
    model_short = model_name.split("/")[-1]

    featurizer = SAEFeaturizer(sae)
    inverse = SAEInverseFeaturizer(sae)

    for task_name, variables in tasks.items():
        for variable in variables:
            task_dir = submission_dir / f"{task_name}_{model_short}_{variable}"
            task_dir.mkdir(parents=True, exist_ok=True)

            torch.save(featurizer.state_dict(), task_dir / "featurizer.pt")
            torch.save(inverse.state_dict(), task_dir / "inverse_featurizer.pt")

            # Save a featurizer.py that MIB can use to reconstruct the module
            _write_loader_script(task_dir, sae)

    print(f"MIB submission prepared at: {submission_dir}")
    print(f"  Model: {model_name}")
    print(f"  Tasks: {list(tasks.keys())}")
    print(f"  Features: {sae.cfg.d_sae}")
    print()
    print("To evaluate:")
    print(f"  python benchmarks/MIB/MIB-causal-variable-track/evaluate_submission.py \\")
    print(f"      --submission_folder {submission_dir}")

    return submission_dir


def _write_loader_script(task_dir: Path, sae: BenchSAE) -> None:
    """Write a featurizer.py that MIB can use to load the featurizer."""
    script = f"""\
# Auto-generated by benchmarks/run_mib.py
# MIB loads this to reconstruct the featurizer module.

import torch
import torch.nn as nn


class Featurizer(nn.Module):
    \"\"\"SAE-based featurizer: x -> (features, error).\"\"\"

    def __init__(self, d_in={sae.cfg.d_in}, d_sae={sae.cfg.d_sae}):
        super().__init__()
        self.d_in = d_in
        self.d_sae = d_sae
        # Weights loaded from featurizer.pt state dict
        self.W_enc = nn.Parameter(torch.zeros(d_in, d_sae))
        self.b_enc = nn.Parameter(torch.zeros(d_sae))
        self.W_dec = nn.Parameter(torch.zeros(d_sae, d_in))
        self.b_dec = nn.Parameter(torch.zeros(d_in))

    def encode(self, x):
        pre_acts = (x - self.b_dec) @ self.W_enc + self.b_enc
        return torch.relu(pre_acts)

    def decode(self, f):
        return f @ self.W_dec + self.b_dec

    def forward(self, x):
        features = self.encode(x)
        reconstruction = self.decode(features)
        error = x - reconstruction
        return features, error


class InverseFeaturizer(nn.Module):
    \"\"\"SAE-based inverse: (features, error) -> x_hat.\"\"\"

    def __init__(self, d_in={sae.cfg.d_in}, d_sae={sae.cfg.d_sae}):
        super().__init__()
        self.d_in = d_in
        self.d_sae = d_sae
        self.W_dec = nn.Parameter(torch.zeros(d_sae, d_in))
        self.b_dec = nn.Parameter(torch.zeros(d_in))

    def forward(self, features, error):
        return features @ self.W_dec + self.b_dec + error
"""
    (task_dir / "featurizer.py").write_text(script)


